func Test_GetStates(t *testing.T) {
	s := &Ground{}
	s.Row = []int{1, 0, 1, 0, 0, 0, 1, 0, 1, 0, 0, 1}
	s.Radius = 3
	data := map[int][]int{
		0:  []int{0, 0, 1, 1, 0, 1, 0},
		2:  []int{1, 1, 0, 1, 0, 0, 0},
		5:  []int{1, 0, 0, 0, 1, 0, 1},
		9:  []int{1, 0, 1, 0, 0, 1, 1},
		11: []int{1, 0, 0, 1, 1, 0, 1},
	}
	for k, v := range data {
		ss := s.getStates(k)
		for i, nv := range ss {
			if nv != v[i] {
				t.Fatal("fail")
			}
		}
	}
}

